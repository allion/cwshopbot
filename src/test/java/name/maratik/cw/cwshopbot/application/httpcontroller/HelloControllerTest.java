package name.maratik.cw.cwshopbot.application.httpcontroller;

import name.maratik.cw.cwshopbot.mock.MockedTest;

import org.junit.Test;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.test.web.servlet.MockMvc;

import static org.springframework.test.web.servlet.request.MockMvcRequestBuilders.get;
import static org.springframework.test.web.servlet.result.MockMvcResultHandlers.log;
import static org.springframework.test.web.servlet.result.MockMvcResultMatchers.content;
import static org.springframework.test.web.servlet.result.MockMvcResultMatchers.status;

public class HelloControllerTest extends MockedTest {

    @Autowired
    private MockMvc mvc;

    @Test
    public void shouldIndexReturn2xx() throws Exception {
        mvc.perform(get("/"))
                .andDo(log())
                .andExpect(status().is2xxSuccessful())
                .andExpect(content().string("Hello world!"));
    }
}
